# User enter two numbers
n = int(input("Please enter the first number: "))
m = int(input("Please enter the second number: "))


print(n , m);
print("Addition: ", n + m)
print("Subtraction: ", n - m)
print("Multiplication: ", n * m)
print("Division: ", n / m)
print("Exponential: ", n ** m)
print("Modulus: ", n % m)
print("Floor Division: ", n // m)
